public class American_flag {
    public static void main(String[] args) {
        // Print first 9 rows with stars and stripes
        for (int i = 0; i < 9; i++) {
            if (i % 2 == 0) {
                System.out.println("* * * * * * ==================================");
            } else {
                System.out.println(" * * * * *  ==================================");
            }
        }
        // Print last 6 rows with just stripes
        for (int i = 0; i < 6; i++) {
            System.out.println("==============================================");
        }
    }
}
